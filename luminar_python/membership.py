# membership operators
# in, not in      in => variable is in(present) output will be true , not in => value is not in (not present) output will be true
string="kavya"
print("a" in string)         #true
print("y" in string)         #true
print("z" not in string)     #true
print("hana" not in string)  #true
print("lekshmi" in string)   #false

# membership operators are only possible in string,list